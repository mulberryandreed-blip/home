function CAT(avg) {
  return avg >= 8 ? { cat: "Ideal client", cls: "b-sage", g: "var(--sage)" } :
    avg >= 7 ? { cat: "Growth client", cls: "b-brass", g: "var(--brass)" } :
    avg >= 5.5 ? { cat: "Maintain client", cls: "b-slate", g: "var(--slate)" } :
    avg >= 4 ? { cat: "Low-fit client", cls: "b-rust", g: "var(--rust)" } :
    { cat: "Exit candidate", cls: "b-ox", g: "var(--oxblood)" };
}
function avgScore(s) { const v = SCORE_DIMS.map(([k]) => s[k]); return Math.round((v.reduce((a, b) => a + b, 0) / v.length) * 10) / 10; }
function clamp(n, min = 0, max = 10) { return isNaN(n) ? min : Math.max(min, Math.min(max, n)); }
function fmt(n) { return "$" + n.toLocaleString(); }

